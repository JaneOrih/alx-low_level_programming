#include "3-calc.h"
#include <stdio.h>
/**
 * op_add - sum function
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - minus function
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication fuction
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division function
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo function
 * @a: parameter
 * @b: parameter
 * Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
